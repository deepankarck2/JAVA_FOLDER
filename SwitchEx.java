public class SwitchEx {
    public static void main(String[] args){
        int dog = 2 ; 

        switch (dog) {
            case 1:
                System.out.println("1\n");
                break;
            case 2:
               System.out.println("2\n");
                break; 
        
            default:
               System.out.println("other\n");
                break;
        }

    }
}
